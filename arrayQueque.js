{
    // Array queue
    const queue = [];

    // Untuk menambahkan data dari belakang dari array
    queue.push("Eko");
    queue.push("Khannedy");
    queue.push("Kurniawan");

    console.log(queue);

    // Untuk menghapus data dari depan dari array dan mengambilnya
    console.log(queue.shift())
    console.log(queue.shift())
    console.log(queue.shift())

    console.log(queue);
}

